#define ISR_PRIORITY_SMU_INT0   10                      /* Define the SMU Service Request 0 interrupt priority  */
#define ISR_PRIORITY_SMU_INT1   9

#define PCn_2_IDX 19
#define P2_IDX 2
#define PCn_1_IDX 11
#define P1_IDX 1
